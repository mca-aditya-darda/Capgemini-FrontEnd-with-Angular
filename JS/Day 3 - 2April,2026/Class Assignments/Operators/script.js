// Arithmetic Operators
let a = 10;
let b = 3;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// Assignment Operators
let x = 5;
x += 2;
x -= 1;
x *= 2;
x /= 3;
console.log(x);

// Comparison Operators
console.log(5 == "5");
console.log(5 === "5");
console.log(5 != "5");
console.log(5 !== "5");
console.log(5 > 3);
console.log(5 < 3);

// Logical Operators
console.log(true && false);
console.log(true || false);
console.log(!true);

// Ternary Operator
let age = 18;
console.log(age >= 18 ? "Adult" : "Minor");

// Unary Operators
let n = 5;
console.log(++n);
console.log(--n);
console.log(typeof n);
